"""
Generate pipeline summary JSON after workflow completion.
This script is called from workflow.onComplete hook in main.nf
"""

import json
import sys
import re
from pathlib import Path
from collections import defaultdict
import argparse


def parse_nanoplot(results_dir):
    # Parse NanoPlot QC results
    # Find NanoStats.txt file recursively in 01_QC directory
    qc_dir = Path(results_dir) / "01_QC"
    nanostat_files = list(qc_dir.rglob("*NanoStats.txt")) if qc_dir.exists() else []

    if not nanostat_files:
        return None

    nanostat_file = nanostat_files[0]

    qc_data = {}
    with open(nanostat_file) as f:
        for line in f:
            if ':' in line:
                key, value = line.split(':', 1)
                key = key.strip()
                value = value.strip()

                # Handle decimal values (remove commas and convert)
                if key == "Number of reads":
                    qc_data['reads_count'] = int(float(value.replace(',', '')))
                elif key == "Total bases":
                    qc_data['total_bases'] = int(float(value.replace(',', '')))
                elif key == "Mean read length":
                    qc_data['mean_length'] = float(value.replace(',', ''))
                elif key == "Median read length":
                    qc_data['median_length'] = float(value.replace(',', ''))
                elif key == "Read length N50":
                    qc_data['n50'] = int(float(value.replace(',', '')))
                elif key == "Mean read quality":
                    qc_data['mean_quality'] = float(value.replace(',', ''))
                    qc_data['mean_qual'] = float(value.replace(',', ''))  # Alias for frontend
                elif key == "Median read quality":
                    qc_data['median_qual'] = float(value.replace(',', ''))

    qc_data['quality_status'] = 'excellent' if qc_data.get('mean_quality', 0) > 10 else 'good'
    return qc_data


def parse_assembly(results_dir):
    # Parse assembly statistics from Flye or QUAST
    # Find .info.txt or assembly_stats.txt file
    assembly_dir = Path(results_dir) / "03_assembly"

    # Prefer JSON stats file generated by pipeline (has exact N50, N90, GC)
    json_stats_files = list(assembly_dir.glob("**/*_assembly_stats.json")) if assembly_dir.exists() else []
    if json_stats_files:
        import json as _json
        with open(json_stats_files[0]) as f:
            stats = _json.load(f)
        data = {
            'contigs_count': stats.get('num_contigs', 0),
            'contigs':       stats.get('num_contigs', 0),
            'total_length':  stats.get('total_length', 0),
            'longest_contig':stats.get('longest_contig', 0),
            'n50':           stats.get('n50', 0),
            'n90':           stats.get('n90', 0),
            'gc_content':    stats.get('gc_content', 0.0),
        }
        if data['contigs_count'] > 0:
            n50 = data['n50']
            total = data['total_length']
            # Quality score: N50 relative to assembly size (higher N50 fraction = better)
            n50_fraction = (n50 / total * 100) if total > 0 else 0
            data['quality_score'] = min(100, int(50 + n50_fraction))
            return data

    assembly_info_files = []
    if assembly_dir.exists():
        assembly_info_files = list(assembly_dir.glob("**/*assembly_stats.txt"))
        if not assembly_info_files:
            assembly_info_files = list(assembly_dir.glob("**/*.info.txt"))

    if assembly_info_files:
        assembly_info = assembly_info_files[0]
        data = {'contigs_count': 0, 'total_length': 0, 'longest_contig': 0, 'n50': 0, 'n90': 0, 'gc_content': 0}

        with open(assembly_info) as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith('='):
                    continue

                if 'Total Length:' in line:
                    data['total_length'] = int(line.split(':')[1].strip().split()[0].replace(',', ''))
                elif 'Number of Contigs:' in line:
                    data['contigs_count'] = int(line.split(':')[1].strip().split()[0].replace(',', ''))
                elif 'N50:' in line:
                    data['n50'] = int(line.split(':')[1].strip().split()[0].replace(',', ''))
                elif 'N90:' in line:
                    data['n90'] = int(line.split(':')[1].strip().split()[0].replace(',', ''))
                elif 'Longest Contig:' in line:
                    data['longest_contig'] = int(line.split(':')[1].strip().split()[0].replace(',', ''))
                elif 'GC Content:' in line:
                    data['gc_content'] = float(line.split(':')[1].strip().replace('%', ''))

        if data['contigs_count'] > 0:
            data['contigs'] = data['contigs_count']
            n50, total = data['n50'], data['total_length']
            n50_fraction = (n50 / total * 100) if total > 0 else 0
            data['quality_score'] = min(100, int(50 + n50_fraction))
            return data

    return {'contigs_count': 0, 'contigs': 0, 'total_length': 0, 'n50': 0, 'n90': 0, 
            'gc_content': 0, 'longest_contig': 0, 'quality_score': 0}


def parse_checkm(results_dir):
    # Parse CheckM quality assessment
    # Search for CheckM summary files in 05_quality directory
    quality_dir = Path(results_dir) / "05_quality"
    
    bins = []
    if quality_dir.exists():
        # Find all CheckM summary files (metabat2, concoct, etc.)
        checkm_files = list(quality_dir.glob("**/*checkm_summary.tsv"))
        
        for checkm_file in checkm_files:
            with open(checkm_file) as f:
                lines = f.readlines()
                # CheckM format: header line, then data lines
                # Skip first line (header) and parse bins
                for line in lines[1:]:
                    if line.strip() and not line.startswith('Bin Id'):
                        parts = line.strip().split('\t')
                        if len(parts) >= 13:  # CheckM has 14 columns
                            bin_id = parts[0]
                            
                            # Skip unbinned contigs
                            if 'unbinned' in bin_id.lower():
                                continue
                            
                            try:
                                # Column 11: Completeness, Column 12: Contamination, Column 13: Strain Heterogeneity
                                completeness = float(parts[11])
                                contamination = float(parts[12])
                                strain_heterogeneity = float(parts[13]) if len(parts) > 13 else 0.0
                                lineage = parts[1]  # Marker lineage
                                
                                # Determine quality
                                if completeness > 90 and contamination < 5:
                                    quality = 'high'
                                elif completeness > 50 and contamination < 10:
                                    quality = 'medium'
                                else:
                                    quality = 'low'
                                
                                bins.append({
                                    'bin_id': bin_id,
                                    'completeness': completeness,
                                    'contamination': contamination,
                                    'strain_heterogeneity': strain_heterogeneity,
                                    'quality': quality,
                                    'size_mb': 2.5,  # Estimated
                                    'lineage': lineage,
                                    'publication_ready': quality == 'high'
                                })
                            except (ValueError, IndexError) as e:
                                print(f"Warning: Failed to parse CheckM line for {bin_id}: {e}")
                                continue
    
    high_q = sum(1 for b in bins if b['quality'] == 'high')
    med_q = sum(1 for b in bins if b['quality'] == 'medium')
    low_q = sum(1 for b in bins if b['quality'] == 'low')
    
    return {
        'total_bins': len(bins),
        'high_quality': high_q,
        'medium_quality': med_q,
        'low_quality': low_q,
        'bins': bins
    }


def parse_abricate(results_dir):
    # Parse ABRicate AMR results
    abricate_dir = Path(results_dir) / "07_amr" / "abricate"

    genes = []
    if abricate_dir.exists():
        for bin_dir in abricate_dir.iterdir():
            if bin_dir.is_dir():
                for file in bin_dir.glob("*.tab"):
                    with open(file) as f:
                        for line in f:
                            if line.startswith('#'):
                                continue
                            parts = line.strip().split('\t')
                            if len(parts) >= 10:
                                antibiotic_info = parts[13] if len(parts) > 13 else 'Unknown'
                                genes.append({
                                    'gene': parts[5] if len(parts) > 5 else 'Unknown',
                                    'bin': bin_dir.name,
                                    'antibiotic_class': antibiotic_info,
                                    'mechanism': antibiotic_info,
                                    'identity': float(parts[9]) if len(parts) > 9 else 0,
                                    'coverage': float(parts[10]) if len(parts) > 10 else 0,
                                    'database': 'card',
                                    'risk_level': 'high' if (len(parts) > 9 and float(parts[9]) > 95) else 'moderate'
                                })

    high_risk = sum(1 for g in genes if g['risk_level'] == 'high')
    mod_risk = len(genes) - high_risk

    # Calculate AMR risk score (0-10)
    risk_score = min(10, (high_risk * 0.5 + mod_risk * 0.2))

    return {
        'total_arg_genes': len(genes),
        'total_genes': len(genes),  # Alias for frontend compatibility
        'high_risk': high_risk,
        'high_risk_genes': high_risk,  # Alias for frontend compatibility
        'moderate_risk': mod_risk,
        'medium_risk_genes': mod_risk,  # Alias for frontend compatibility
        'genes': genes[:20]  # Limit to top 20
    }, risk_score


def parse_kraken(results_dir):
    # Parse Kraken2 taxonomy results
    kraken2_dir = Path(results_dir) / "06_kraken2"
    
    species = []
    if kraken2_dir.exists():
        # Find all Kraken2 summary files (metabat2, concoct)
        kraken_summaries = list(kraken2_dir.glob("**/*_kraken2_summary.tsv"))
        
        for summary_file in kraken_summaries:
            with open(summary_file) as f:
                lines = f.readlines()
                # Skip header and process data lines
                for line in lines[1:]:
                    if line.strip() and not line.startswith('bin_id'):
                        parts = line.strip().split('\t')
                        if len(parts) >= 5:
                            bin_id = parts[0]
                            taxonomy = parts[1]
                            confidence = float(parts[2]) if parts[2] != '0' else 0.0
                            num_reads = int(parts[3]) if parts[3].isdigit() else 0
                            
                            # Skip failed classifications or low confidence
                            if 'failed' in taxonomy.lower() or confidence < 0.1 or num_reads < 10:
                                continue
                            
                            # Determine pathogenicity (simplified list)
                            pathogens = ['Escherichia', 'Klebsiella', 'Pseudomonas', 
                                        'Staphylococcus', 'Enterococcus', 'Salmonella',
                                        'Listeria', 'Mycobacterium', 'Clostridioides',
                                        'Vibrio', 'Campylobacter', 'Helicobacter']
                            is_pathogen = any(p in taxonomy for p in pathogens)
                            
                            # Calculate abundance percentage (simplified)
                            abundance = min(confidence * 100, 100.0)
                            
                            species.append({
                                'bin': bin_id,
                                'name': taxonomy,
                                'abundance': abundance,
                                'pathogenicity': 'pathogen' if is_pathogen else 'commensal',
                                'risk_level': 'high' if is_pathogen else 'low',
                                'clinical_relevance': 'Healthcare-associated infection risk' if is_pathogen else 'Low risk'
                            })
    
    # Sort by abundance and take top 15
    species.sort(key=lambda x: x['abundance'], reverse=True)
    species = species[:15]
    
    high_risk_count = sum(1 for s in species if s['risk_level'] == 'high')
    
    return {
        'species': species,
        'risk_assessment': {
            'high': high_risk_count,
            'medium': 0,
            'low': len(species) - high_risk_count
        }
    }


def calculate_quality_score(qc, assembly, mags):
    # Calculate overall pipeline quality score (0-100)
    score = 0

    # QC contributes 30%
    if qc:
        qc_score = min(30, (qc.get('mean_quality', 0) / 15) * 30)
        score += qc_score

    # Assembly contributes 30%
    if assembly and assembly['contigs_count'] > 0:
        assembly_score = min(30, (assembly['quality_score'] / 100) * 30)
        score += assembly_score

    # MAGs quality contributes 40%
    if mags and mags['total_bins'] > 0:
        mags_score = (mags['high_quality'] / max(mags['total_bins'], 1)) * 40
        score += mags_score

    return round(score, 1)


def main():
    parser = argparse.ArgumentParser(description='Generate pipeline summary JSON')
    parser.add_argument('--sample-id', required=True, help='Sample ID')
    parser.add_argument('--results-dir', required=True, help='Results directory')
    parser.add_argument('--output', required=True, help='Output JSON file path')

    args = parser.parse_args()

    sample_id = args.sample_id
    results_dir = Path(args.results_dir)
    output_file = Path(args.output)

    print(f"Generating summary for {sample_id}...")
    print(f"Results directory: {results_dir}")

    # Parse all components
    qc = parse_nanoplot(results_dir)
    assembly = parse_assembly(results_dir)
    mags = parse_checkm(results_dir)
    amr_data, amr_risk = parse_abricate(results_dir)
    taxonomy = parse_kraken(results_dir)

    # Calculate scores
    quality_score = calculate_quality_score(qc, assembly, mags)

    # Generate recommendations
    recommendations = []
    if amr_risk >= 7:
        recommendations.append("🔴 Critical: High AMR risk detected. Implement strict infection control.")
    if mags.get('high_quality', 0) == 0:
        recommendations.append("🟡 Warning: No high-quality MAGs. Consider deeper sequencing.")
    if taxonomy.get('risk_assessment', {}).get('high', 0) > 0:
        recommendations.append("🔴 Pathogens detected. Clinical assessment recommended.")

    # Build final summary
    summary = {
        "sample_id": sample_id,
        "status": "completed",
        "quality_score": quality_score,
        "amr_risk_score": round(amr_risk, 1),
        "qc": qc or {},
        "assembly": assembly,
        "mags": mags,
        "amr": amr_data,
        "taxonomy": taxonomy,
        "recommendations": recommendations
    }

    # Create output directory if needed
    output_file.parent.mkdir(parents=True, exist_ok=True)

    # Write JSON
    with open(output_file, "w") as f:
        json.dump(summary, f, indent=2)

    print(f"✅ Summary created successfully: {output_file}")
    print(f"   Quality score: {quality_score}")
    print(f"   AMR risk: {amr_risk}")
    print(f"   MAGs: {mags['total_bins']} bins ({mags['high_quality']} high quality)")

    return 0


if __name__ == "__main__":
    sys.exit(main())
